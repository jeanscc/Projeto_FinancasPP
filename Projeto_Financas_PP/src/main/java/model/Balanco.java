package model;

public class Balanco {
	private double total;
	private double retorno;
	private double lucro;
	private double investido;
	private double pendente;
	private double perca;
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	public double getRetorno() {
		return retorno;
	}
	public void setRetorno(double retorno) {
		this.retorno = retorno;
	}
	public double getLucro() {
		return lucro;
	}
	public void setLucro(double lucro) {
		this.lucro = lucro;
	}
	public double getInvestido() {
		return investido;
	}
	public void setInvestido(double investido) {
		this.investido = investido;
	}
	public double getPendente() {
		return pendente;
	}
	public void setPendente(double pendente) {
		this.pendente = pendente;
	}
	public double getPerca() {
		return perca;
	}
	public void setPerca(double perca) {
		this.perca = perca;
	}
	
	
}
